// Um literal em JavaScript é um valor escrito diretamente no código, sem precisar de variável.

// Em JavaScript, os literais são:

// Number: 
10; 3.14; NaN; Infinity

// BigInt:
10n

// String:
"texto"; 'texto'; `texto`

// Boolean:
true; false

// Null:
null

// Undefined:
undefined

// Object:
{}; { a: 1 }

// Array:
[]; [1, 2]

// Function:
    // function() {}; () => {}    [Comentei para tirar o "erro"]

// RegExp:
/abc/

// Symbol:
Symbol()